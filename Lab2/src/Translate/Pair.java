package Translate;

public class Pair {
    
  public String spa;
  public String ing;

  public Pair(String spa, String ing) {

    this.spa = spa;
    this.ing = ing;
  }

  public void setSpa(String spa) {
    this.spa = spa;
  }

  public void setIng(String ing) {
    this.ing = ing;
  }

  public String getSpa() {
    return spa;
  }

  public String getIng() {
    return ing;
  }
    
}